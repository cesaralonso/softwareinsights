          <h1 class="py-3">¿Qué es UX?</h1>
          <p>UX (por sus siglas en inglés User Experience o en español Experiencia de Usuario) 
              es aquello que una persona percibe al interactuar con un producto o servicio.
              Logramos una buena UX al enfocarnos en diseñar productos útiles, usables y deseables, 
              lo cual influye en que el usuario se sienta satisfecho, feliz y encantado.

          </p> 
          <p>UX es aquello que una persona percibe al interactuar con un producto o servicio.</p>
          <h2 class="py-3">¿Cómo se logra esta experiencia?</h2>
          <p> Una buena UX se logra a través del Diseño Centrado en el Humano, el cual es el enfoque de
                  conocer las necesidades de los usuarios y alinearlos a los objetivos del negocio tomando 
                  también en cuenta las limitaciones técnicas.
          </p>
          <p>La persona que realiza UX, es conocida como un UX Designer y como parte de sus responsabilidades está el 
              de investigar qué es lo que las personas necesitan para cumplir sus objetivos y resolver sus problemas.
          </p>
          <p>    
              <h4>Dentro de las actividades que se realizan en UX, están:</h4>
              <br>1.- Investigación (con stakeholders, etnográfica, entrevistas 1 a 1, etc).
              <br>2.- Pruebas (de usabilidad, con usuarios).
              <br>3.- Experimentos (A/B, con prototipos).
              <br>4.- Análisis de datos (KPI’s, métricas).
              <br>5.- Arquitectura de información.
          </p>
          <p> Una persona que se desempeña como UX Designer tiene conocimientos generales de:
              <br>*Psicología, Sociología o Antropología
              <br>*Tecnología, Desarrollo de Productos Digitales
              <br>*Comunicación, Marketing
              <br>*Negocios, Ventas
              <br>*Diseño Industrial, Gráfico 
              <br>*Conoce, las 10 habilidades que un UX Designer debe tener.
          </p>       
          <h1 class="py-3">¿Qué es UI?</h1>
          <p> UI (por sus siglas en inglés User Interface) o en español 
              Interfaz del Usuario, es la vista que permite a un usuario interactuar de manera 
              efectiva con un sistema.
              <br>Es la suma de una arquitectura de información + elementos visuales + patrones de interacción.
          </p> 
          <p> El UI da el ‘look & feel’ al producto con la estructura e interacción de los elementos de la interfaz.</p>

          <p>
              Un UI Designer se encarga de crear visualmente la interfaz del producto para que vaya acorde a la 
              experiencia del usuario. Además, crea elementos interactivos y se preocupa de que se vean bien en 
              todas las plataformas (móvil, tableta, web). Un UI Designer trabaja muy de la mano con el equipo de 
              desarrollo o diseño de producto proporcionando guías de estilos y patrones de uso.
          </p>

          <h4 class="py-3">Dentro de las actividades que se realizan en UI, están:</h4>
          <p>
              <br>1.- Diseño de interacción (cómo responde el sistema)
              <br>2.- Guías de interacción (estados del sistema)
              <br>3.- Diseño de elementos (botones, formularios)
              <br>4.- Diseño visual (iconos, imágenes)
              <br>5.- Guías de estilo (paletas de color, fonts)
              <br>6.- Un UI Designer por lo general tiene estudios en:
              <br>7.- Diseño gráfico, Artes visuales
              <br>8.- Diseño industrial
              <br>9.- Tecnología, Desarrollo de Productos Digitales
          </p>
          <h1>En conclusión</h1>
          <p>
              Una buena UI nos permite dar una buena UX.
          </p>
          <p>
              UX es lo qué te hace sentir una marca o producto al momento de interactuar con él,
              mientras que UI es una capa visual de colores, texturas, formas y elementos.
          </p>
          <p>
              UX no es lo mismo que UI. Detrás de un producto o aplicación increíble tuvo que haber
                trabajo previo de investigación y una correcta estrategia que se tradujo en una interfaz 
                atractiva. UI no es, ni nunca será por sí sola una solución.
          </p>
